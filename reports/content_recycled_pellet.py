_YEARS = list(range(2009, 2026))
def _yi(r):
    try: return int(float(r.get("year", "")))
    except Exception: return None
def _txt(r):
    return (r.get("発明の名称", "") + " " + r.get("要約", "") + " "
            + r.get("core_解決手段分類", "") + " " + r.get("core_技術分類", "") + " "
            + r.get("core_課題分類", ""))
def _has(r, ks):
    t = _txt(r); return any(k in t for k in ks)
def _annual(sel):
    c = _col.Counter(_yi(r) for r in _rows if sel(r) and _yi(r))
    return {y: c.get(y, 0) for y in _YEARS}

def _anchor(cid, pts, dx=0.0, dy=0.0):
    """drill_cluster cid の重心を意味マップ正規化座標(0-1, y上反転)で返す。box は dx/dy でずらす。"""
    xs = [p[0] for p in pts]; ys = [p[1] for p in pts]
    xmin, xmax = min(xs), max(xs); ymin, ymax = min(ys), max(ys)
    padx = (xmax - xmin) * 0.07 or 1.0; pady = (ymax - ymin) * 0.10 or 1.0
    xmin -= padx; xmax += padx; ymin -= pady; ymax += pady
    cc = [(p[0], p[1]) for p in pts if p[2] == cid]
    if not cc: return (0.5, 0.5), (0.5 + dx, 0.5 + dy)
    ox = sum(p[0] for p in cc) / len(cc); oy = sum(p[1] for p in cc) / len(cc)
    ax = (ox - xmin) / (xmax - xmin); ay = 1 - (oy - ymin) / (ymax - ymin)
    return (ax, ay), (min(max(ax + dx, 0.02), 0.96), min(max(ay + dy, 0.05), 0.95))

